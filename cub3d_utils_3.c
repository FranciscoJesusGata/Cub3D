#include "cub3d.h"

int		ft_count_chars(const char *s, char c)
{
	int	i;
	int	fnd;

	i = 0;
	fnd = 0;
	while (s[i])
	{
		if (s[i] == c)
			fnd++;
		i++;
	}
	return (fnd);
}